const getFileIcon = (filename: string): (() => JSX.Element) => {
  if (!filename) return UnknownFile;
  const position = filename.lastIndexOf(".");
  if (position < 0) return UnknownFile;
  const ext = filename.substr(position).toLowerCase();
  switch (ext) {
    case ".d.ts":
    case ".ts":
      return TsFile;
    case ".js":
      return JsFile;
    case ".json":
      return JsFile;
    case ".xml":
      return XmlFile;
    case ".html":
    case ".htm":
      return HtmlFile;
    case ".yml":
    case ".yaml":
      return JsFile;
  }
  return UnknownFile;
}

